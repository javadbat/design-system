import { existsSync, mkdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, isAbsolute, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { execFileSync } from "node:child_process";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, "../..");
const validatorPath = resolve(scriptDir, "validate-commit-message.mjs");
const submoduleFile = join(repoRoot, ".gitmodules");

function git(args, cwd = repoRoot) {
  return execFileSync("git", args, { cwd, encoding: "utf8" }).trim();
}

function toPosixPath(path) {
  return path.replace(/\\/g, "/");
}

function quoteForShell(path) {
  return `'${toPosixPath(path).replace(/'/g, "'\\''")}'`;
}

function getGitDir(worktreePath) {
  const output = git(["rev-parse", "--git-dir"], worktreePath);
  return isAbsolute(output) ? output : resolve(worktreePath, output);
}

function installCommitMsgHook(worktreePath) {
  const gitDir = getGitDir(worktreePath);
  const hooksDir = join(gitDir, "hooks");
  const hookPath = join(hooksDir, "commit-msg");
  const relativeWorktree = relative(repoRoot, worktreePath) || ".";

  mkdirSync(hooksDir, { recursive: true });
  writeFileSync(
    hookPath,
    `#!/bin/sh
# Generated by scripts/git-hooks/install-git-hooks.mjs
node ${quoteForShell(validatorPath)} "$1"
`,
    { mode: 0o755 },
  );

  console.log(`installed commit-msg hook: ${relativeWorktree}`);
}

function getSubmodulePaths() {
  if (!existsSync(submoduleFile)) {
    return [];
  }

  const content = readFileSync(submoduleFile, "utf8");
  return [...content.matchAll(/^\s*path\s*=\s*(.+)\s*$/gm)].map(match => match[1].trim());
}

installCommitMsgHook(repoRoot);

for (const submodulePath of getSubmodulePaths()) {
  const absoluteSubmodulePath = resolve(repoRoot, submodulePath);

  if (!existsSync(absoluteSubmodulePath) || !statSync(absoluteSubmodulePath).isDirectory()) {
    console.log(`skipped missing submodule: ${submodulePath}`);
    continue;
  }

  try {
    installCommitMsgHook(absoluteSubmodulePath);
  } catch (error) {
    console.log(`skipped uninitialized submodule: ${submodulePath}`);
  }
}
